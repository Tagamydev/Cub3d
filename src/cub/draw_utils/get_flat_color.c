#include <cub.h>

int		get_flat_color(int color)
{
	int r;
	int g;
	int b;
	int t;

	t = 0;
	r = ((color >> 16) & 0xFF);
	g = ((color >> 8) & 0xFF);
	b = ((color) & 0xFF);
	return (t << 24 | r << 16 | g << 8 | b);
}
